function addLoadEventCallback(func) {
    var oldonload = window.onload;
    if (typeof window.onload != 'function') {
      window.onload = func;
    } else {
      window.onload = function() {
        if (oldonload) {
          oldonload();
        }
        func();
      }
    }
}

var delay = (time) => {
  return new Promise(resolve => setTimeout(resolve, time));
}